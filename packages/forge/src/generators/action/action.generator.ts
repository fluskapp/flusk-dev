/**
 * Action generator — creates GitHub Action files from an action YAML schema.
 */
import { resolve } from 'node:path';
import { readFile, writeFile, mkdir } from 'node:fs/promises';
import { parse as parseYaml } from 'yaml';
import { toPascalCase, toCamelCase } from '../utils.js';

export interface ActionInput {
  name: string;
  description: string;
  required?: boolean;
  default?: string;
}

export interface ActionOutput {
  name: string;
  description: string;
}

export interface ActionSchema {
  name: string;
  description: string;
  inputs: ActionInput[];
  outputs: ActionOutput[];
}

export interface GenResult {
  files: { path: string; action: 'created' }[];
}

async function loadSchema(yamlPath: string): Promise<ActionSchema> {
  return parseYaml(await readFile(yamlPath, 'utf-8')) as ActionSchema;
}

export function generateActionYmlContent(schema: ActionSchema): string {
  const inputLines = schema.inputs.map(inp => {
    const lines = [`    ${inp.name}:\n      description: '${inp.description}'`];
    if (inp.required) lines.push(`      required: true`);
    if (inp.default !== undefined) lines.push(`      default: '${inp.default}'`);
    return lines.join('\n');
  });

  const outputLines = schema.outputs.map(out =>
    `    ${out.name}:\n      description: '${out.description}'`
  );

  return `# --- BEGIN GENERATED ---
# @generated by @flusk/forge — action generator
name: 'Flusk ${toPascalCase(schema.name)}'
description: '${schema.description}'
inputs:
${inputLines.join('\n')}
outputs:
${outputLines.join('\n')}
runs:
  using: 'node20'
  main: 'dist/index.js'
# --- END GENERATED ---
`;
}

export function generateActionIndexContent(schema: ActionSchema): string {
  const pascal = toPascalCase(schema.name);
  const getInputLines = schema.inputs.map(inp => {
    const camel = toCamelCase(inp.name);
    const required = inp.required ? 'true' : 'false';
    return `    ${camel}: core.getInput('${inp.name}'${inp.required ? ', { required: true }' : ''}),`;
  });

  const setOutputLines = schema.outputs.map(out => {
    const camel = toCamelCase(out.name);
    return `    core.setOutput('${out.name}', results.${camel});`;
  });

  return `// --- BEGIN GENERATED ---
/**
 * GitHub Action entrypoint: ${schema.name}
 * ${schema.description}
 * @generated by @flusk/forge — action generator
 */
import * as core from '@actions/core';
import type { ${pascal}Inputs, ${pascal}Outputs } from './types.js';

async function run(): Promise<void> {
  try {
    const inputs: ${pascal}Inputs = {
${getInputLines.join('\n')}
    };

    // TODO: implement action logic
    const results: ${pascal}Outputs = {
${schema.outputs.map(out => `      ${toCamelCase(out.name)}: '',`).join('\n')}
    };

${setOutputLines.join('\n')}
  } catch (error) {
    if (error instanceof Error) core.setFailed(error.message);
  }
}

run();
// --- END GENERATED ---
`;
}

export function generateActionTypesContent(schema: ActionSchema): string {
  const pascal = toPascalCase(schema.name);

  const inputFields = schema.inputs.map(inp => {
    return `  /** ${inp.description} */\n  ${toCamelCase(inp.name)}: string;`;
  });

  const outputFields = schema.outputs.map(out => {
    return `  /** ${out.description} */\n  ${toCamelCase(out.name)}: string;`;
  });

  return `// --- BEGIN GENERATED ---
/**
 * Types for the ${schema.name} GitHub Action
 * @generated by @flusk/forge — action generator
 */
export interface ${pascal}Inputs {
${inputFields.join('\n')}
}

export interface ${pascal}Outputs {
${outputFields.join('\n')}
}
// --- END GENERATED ---
`;
}

export function generateActionTestContent(schema: ActionSchema): string {
  const pascal = toPascalCase(schema.name);
  return `// --- BEGIN GENERATED ---
/**
 * @generated by @flusk/forge — action generator
 */
import { describe, it, expect } from 'vitest';
import type { ${pascal}Inputs, ${pascal}Outputs } from '../src/types.js';

describe('${schema.name} action types', () => {
  it('should define input type', () => {
    const inputs: ${pascal}Inputs = {
${schema.inputs.map(inp => `      ${toCamelCase(inp.name)}: 'test',`).join('\n')}
    };
    expect(inputs).toBeDefined();
  });

  it('should define output type', () => {
    const outputs: ${pascal}Outputs = {
${schema.outputs.map(out => `      ${toCamelCase(out.name)}: 'test',`).join('\n')}
    };
    expect(outputs).toBeDefined();
  });
});
// --- END GENERATED ---
`;
}

async function writeGenFile(root: string, relPath: string, content: string, result: GenResult): Promise<void> {
  const abs = resolve(root, relPath);
  await mkdir(resolve(abs, '..'), { recursive: true });
  await writeFile(abs, content, 'utf-8');
  result.files.push({ path: relPath, action: 'created' });
}

export async function generateAction(yamlPath: string): Promise<GenResult> {
  const root = process.cwd();
  const schema = await loadSchema(yamlPath);
  const result: GenResult = { files: [] };

  await writeGenFile(root, `.github/actions/${schema.name}/action.yml`, generateActionYmlContent(schema), result);
  await writeGenFile(root, `.github/actions/${schema.name}/src/index.ts`, generateActionIndexContent(schema), result);
  await writeGenFile(root, `.github/actions/${schema.name}/src/types.ts`, generateActionTypesContent(schema), result);
  await writeGenFile(root, `.github/actions/${schema.name}/__tests__/action.test.ts`, generateActionTestContent(schema), result);

  return result;
}
