/**
 * Render types and test content for GitHub Actions.
 */

import type { ActionSchema } from './action.types.js';
import { toPascalCase, toCamelCase } from '../utils.js';

export { generateActionYmlContent, generateActionIndexContent } from './action-render-yml.js';

export function generateActionTypesContent(schema: ActionSchema): string {
  const pascal = toPascalCase(schema.name);

  const inputFields = schema.inputs.map(inp => {
    return `  /** ${inp.description} */\n  ${toCamelCase(inp.name)}: string;`;
  });

  const outputFields = schema.outputs.map(out => {
    return `  /** ${out.description} */\n  ${toCamelCase(out.name)}: string;`;
  });

  return `// --- BEGIN GENERATED ---
/**
 * Types for the ${schema.name} GitHub Action
 * @generated by @flusk/forge — action generator
 */
export interface ${pascal}Inputs {
${inputFields.join('\n')}
}

export interface ${pascal}Outputs {
${outputFields.join('\n')}
}
// --- END GENERATED ---
`;
}

export function generateActionTestContent(schema: ActionSchema): string {
  const pascal = toPascalCase(schema.name);
  return `// --- BEGIN GENERATED ---
/**
 * @generated by @flusk/forge — action generator
 */
import { describe, it, expect } from 'vitest';
import type { ${pascal}Inputs, ${pascal}Outputs } from '../src/types.js';

describe('${schema.name} action types', () => {
  it('should define input type', () => {
    const inputs: ${pascal}Inputs = {
${schema.inputs.map(inp => `      ${toCamelCase(inp.name)}: 'test',`).join('\n')}
    };
    expect(inputs).toBeDefined();
  });

  it('should define output type', () => {
    const outputs: ${pascal}Outputs = {
${schema.outputs.map(out => `      ${toCamelCase(out.name)}: 'test',`).join('\n')}
    };
    expect(outputs).toBeDefined();
  });
});
// --- END GENERATED ---
`;
}
