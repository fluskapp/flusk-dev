/**
 * Render action.yml and index.ts content for GitHub Actions.
 */

import type { ActionSchema } from './action.types.js';
import { toPascalCase, toCamelCase } from '../utils.js';

export function generateActionYmlContent(schema: ActionSchema): string {
  const inputLines = schema.inputs.map(inp => {
    const lines = [`    ${inp.name}:\n      description: '${inp.description}'`];
    if (inp.required) lines.push(`      required: true`);
    if (inp.default !== undefined) lines.push(`      default: '${inp.default}'`);
    return lines.join('\n');
  });

  const outputLines = schema.outputs.map(out =>
    `    ${out.name}:\n      description: '${out.description}'`
  );

  return `# --- BEGIN GENERATED ---
# @generated by @flusk/forge — action generator
name: 'Flusk ${toPascalCase(schema.name)}'
description: '${schema.description}'
inputs:
${inputLines.join('\n')}
outputs:
${outputLines.join('\n')}
runs:
  using: 'node20'
  main: 'dist/index.js'
# --- END GENERATED ---
`;
}

export function generateActionIndexContent(schema: ActionSchema): string {
  const pascal = toPascalCase(schema.name);
  const getInputLines = schema.inputs.map(inp => {
    const camel = toCamelCase(inp.name);
    const _required = inp.required ? 'true' : 'false';
    return `    ${camel}: core.getInput('${inp.name}'${inp.required ? ', { required: true }' : ''}),`;
  });

  const setOutputLines = schema.outputs.map(out => {
    const camel = toCamelCase(out.name);
    return `    core.setOutput('${out.name}', results.${camel});`;
  });

  return `// --- BEGIN GENERATED ---
/**
 * GitHub Action entrypoint: ${schema.name}
 * ${schema.description}
 * @generated by @flusk/forge — action generator
 */
import * as core from '@actions/core';
import type { ${pascal}Inputs, ${pascal}Outputs } from './types.js';

async function run(): Promise<void> {
  try {
    const inputs: ${pascal}Inputs = {
${getInputLines.join('\n')}
    };

    // TODO: implement action logic
    const results: ${pascal}Outputs = {
${schema.outputs.map(out => `      ${toCamelCase(out.name)}: '',`).join('\n')}
    };

${setOutputLines.join('\n')}
  } catch (error) {
    if (error instanceof Error) core.setFailed(error.message);
  }
}

run();
// --- END GENERATED ---
`;
}
