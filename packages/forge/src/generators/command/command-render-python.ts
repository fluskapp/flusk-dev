/**
 * Render Python click command file.
 */

import type { CommandSchema } from './command.types.js';

export function generateCommandPythonContent(schema: CommandSchema): string {
  const optionDecorators = schema.options.map(opt => {
    const _pyName = opt.name.replace(/-/g, '_');
    if (opt.type === 'boolean') {
      return `@click.option('--${opt.name}', is_flag=True, default=${opt.default ? 'True' : 'False'}, help='${opt.description || opt.name}')`;
    }
    const pyType = opt.type === 'integer' ? 'int' : opt.type === 'number' ? 'float' : 'str';
    const parts = [`@click.option('--${opt.name}'`];
    parts.push(`, type=${pyType}`);
    if (opt.default !== undefined) parts.push(`, default=${JSON.stringify(opt.default)}`);
    if (opt.enum) parts.push(`, type=click.Choice([${opt.enum.map(v => `'${v}'`).join(', ')}])`);
    if (!opt.required && opt.default === undefined) parts.push(`, default=None`);
    parts.push(`, help='${opt.description || opt.name}'`);
    parts.push(')');
    return parts.join('');
  });

  const paramNames = schema.options.map(opt => opt.name.replace(/-/g, '_'));

  return `# --- BEGIN GENERATED ---
# ${schema.name} command
# @generated by @flusk/forge — command generator

import click


${optionDecorators.join('\n')}
@click.command('${schema.name}')
def ${schema.name.replace(/-/g, '_')}(${paramNames.join(', ')}):
    """${schema.description}"""
    # TODO: implement command logic
    click.echo(f'${schema.name} command executed')

# --- END GENERATED ---
`;
}
