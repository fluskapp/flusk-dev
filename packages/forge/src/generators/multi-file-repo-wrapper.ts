/**
 * Wrapper repository generator — creates a single-file re-export
 * from multi-file repo with entity-prefixed function names.
 *
 * Routes use `ExperimentRepository.createExperiment(db, data)`
 * Multi-file repos export `create(db, data)`
 * This wrapper bridges the gap.
 */

import type { EntitySchema } from '../schema/entity-schema.types.js';
import { toKebab, normalizeQueries } from './multi-file-repo-helpers.js';

/** Generate wrapper repository file */
export function generateRepoWrapper(schema: EntitySchema): string {
  const n = schema.name;
  const kebab = toKebab(n);
  const plural = `${n}s`;
  const lines: string[] = [];

  lines.push(`/**`);
  lines.push(` * @generated by @flusk/forge — repo wrapper`);
  lines.push(` * Re-exports from multi-file repo with entity-prefixed names`);
  lines.push(` */`);
  lines.push(``);
  lines.push(`// --- BEGIN GENERATED (do not edit) ---`);
  lines.push(`import * as Repo from '../sqlite/repositories/${kebab}/index.js';`);
  lines.push(`import type { DatabaseSync } from 'node:sqlite';`);
  lines.push(`import type { ${n}Entity } from '@flusk/entities';`);
  lines.push(``);

  // CRUD operations
  lines.push(`type CreateInput = Omit<${n}Entity, 'id' | 'createdAt' | 'updatedAt'>;`);
  lines.push(`type UpdateInput = Partial<CreateInput>;`);
  lines.push(``);
  lines.push(`export function create${n}(db: DatabaseSync, data: CreateInput): ${n}Entity {`);
  lines.push(`  return Repo.create(db, data);`);
  lines.push(`}`);
  lines.push(``);
  lines.push(`export function find${n}ById(db: DatabaseSync, id: string): ${n}Entity | null {`);
  lines.push(`  return Repo.findById(db, id);`);
  lines.push(`}`);
  lines.push(``);
  lines.push(`export function list${plural}(db: DatabaseSync, limit?: number, offset?: number): ${n}Entity[] {`);
  lines.push(`  return Repo.list(db, limit, offset);`);
  lines.push(`}`);
  lines.push(``);
  lines.push(`export function update${n}(db: DatabaseSync, id: string, data: UpdateInput): ${n}Entity | null {`);
  lines.push(`  return Repo.update(db, id, data);`);
  lines.push(`}`);
  lines.push(``);
  lines.push(`export function delete${n}(db: DatabaseSync, id: string): boolean {`);
  lines.push(`  return Repo.deleteById(db, id);`);
  lines.push(`}`);

  // Time-range if enabled
  if (schema.capabilities?.['time-range']) {
    lines.push(``);
    lines.push(`export function find${plural}ByTimeRange(db: DatabaseSync, from: string, to: string): ${n}Entity[] {`);
    lines.push(`  return Repo.findByTimeRange(db, from, to);`);
    lines.push(`}`);
  }

  // Custom queries pass-through
  const queries = normalizeQueries(schema.queries);
  for (const q of queries) {
    lines.push(``);
    const params = q.params
      ? Object.entries(q.params).map(([k, v]) => `${k}: ${(v as { type: string }).type}`).join(', ')
      : '';
    const args = q.params ? Object.keys(q.params).join(', ') : '';
    const returnType = q.returns === 'scalar' ? 'unknown'
      : q.returns === 'single' ? `${n}Entity | null`
      : q.returns === 'list' ? `${n}Entity[]`
      : 'unknown[]';
    lines.push(`export function ${q.name}(db: DatabaseSync${params ? ', ' + params : ''}): ${returnType} {`);
    lines.push(`  return Repo.${q.name}(db${args ? ', ' + args : ''}) as ${returnType};`);
    lines.push(`}`);
  }

  lines.push(`// --- END GENERATED ---`);
  lines.push(``);

  return lines.join('\n');
}
