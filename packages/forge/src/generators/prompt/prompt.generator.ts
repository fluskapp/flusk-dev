/**
 * Prompt generator — creates prompt template files from a prompt YAML schema.
 */
import { resolve } from 'node:path';
import { readFile, writeFile, mkdir } from 'node:fs/promises';
import { parse as parseYaml } from 'yaml';
import { toPascalCase, toCamelCase } from '../utils.js';

export interface PromptVariable {
  name: string;
  type: string;
  description?: string;
}

export interface PromptSchema {
  name: string;
  domain: string;
  description: string;
  template: string;
  variables: PromptVariable[];
}

export interface GenResult {
  files: { path: string; action: 'created' }[];
}

async function loadSchema(yamlPath: string): Promise<PromptSchema> {
  return parseYaml(await readFile(yamlPath, 'utf-8')) as PromptSchema;
}

export function generatePromptTemplateContent(schema: PromptSchema): string {
  const pascal = toPascalCase(schema.name);
  const camel = toCamelCase(schema.name);
  const params = schema.variables.map(v => `  ${toCamelCase(v.name)}: vars.${toCamelCase(v.name)},`);

  return `// --- BEGIN GENERATED ---
/**
 * ${schema.description}
 * @generated by @flusk/forge — prompt generator
 */
import type { ${pascal}Variables } from './${schema.name}.types.js';

export const ${camel}Template = ${JSON.stringify(schema.template)};

/**
 * Render the ${schema.name} prompt with the given variables.
 */
export function render${pascal}(vars: ${pascal}Variables): string {
  let result = ${camel}Template;
${schema.variables.map(v => `  result = result.replace(/\\{\\{${v.name}\\}\\}/g, vars.${toCamelCase(v.name)});`).join('\n')}
  return result;
}
// --- END GENERATED ---
`;
}

export function generatePromptTypesContent(schema: PromptSchema): string {
  const pascal = toPascalCase(schema.name);
  const fields = schema.variables.map(v => {
    const tsType = v.type === 'number' ? 'number' : v.type === 'boolean' ? 'boolean' : 'string';
    const doc = v.description ? `  /** ${v.description} */\n` : '';
    return `${doc}  ${toCamelCase(v.name)}: ${tsType};`;
  });

  return `// --- BEGIN GENERATED ---
/**
 * Variable types for ${schema.name} prompt
 * @generated by @flusk/forge — prompt generator
 */
export interface ${pascal}Variables {
${fields.join('\n')}
}
// --- END GENERATED ---
`;
}

export function generatePromptPythonContent(schema: PromptSchema): string {
  const funcName = schema.name.replace(/-/g, '_');
  const params = schema.variables.map(v => `    ${v.name.replace(/-/g, '_')}: str`);

  return `# --- BEGIN GENERATED ---
# ${schema.description}
# @generated by @flusk/forge — prompt generator

${funcName.toUpperCase()}_TEMPLATE = ${JSON.stringify(schema.template)}


def render_${funcName}(
${params.join(',\n')},
) -> str:
    """Render the ${schema.name} prompt with the given variables."""
    result = ${funcName.toUpperCase()}_TEMPLATE
${schema.variables.map(v => `    result = result.replace('{{${v.name}}}', ${v.name.replace(/-/g, '_')})`).join('\n')}
    return result

# --- END GENERATED ---
`;
}

async function writeGenFile(root: string, relPath: string, content: string, result: GenResult): Promise<void> {
  const abs = resolve(root, relPath);
  await mkdir(resolve(abs, '..'), { recursive: true });
  await writeFile(abs, content, 'utf-8');
  result.files.push({ path: relPath, action: 'created' });
}

export async function generatePrompt(yamlPath: string): Promise<GenResult> {
  const root = process.cwd();
  const schema = await loadSchema(yamlPath);
  const result: GenResult = { files: [] };
  const domain = schema.domain;

  await writeGenFile(root, `packages/business-logic/src/${domain}/prompts/${schema.name}.prompt.ts`, generatePromptTemplateContent(schema), result);
  await writeGenFile(root, `packages/business-logic/src/${domain}/prompts/${schema.name}.types.ts`, generatePromptTypesContent(schema), result);
  await writeGenFile(root, `flusk-py/src/flusk/prompts/${schema.name}.py`, generatePromptPythonContent(schema), result);

  return result;
}
