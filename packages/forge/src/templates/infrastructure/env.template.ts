/**
 * Template for .env.example generation.
 * Composes sections from sub-modules.
 */

export type { EnvTemplateOptions } from './env-types.js';
import type { EnvTemplateOptions } from './env-types.js';
import { envAppSection, envDatabaseSection, envCacheSection } from './env-sections-app.js';
import { envLlmSection, envFluskSection, envSecuritySection, envLoggingSection } from './env-sections-services.js';
import { envDockerSection, envDevToolsSection, envTestingSection } from './env-sections-dev.js';

/**
 * Generate .env.example template for Flusk projects
 */
export function generateEnvTemplate(options: EnvTemplateOptions): string {
  const {
    projectName,
    postgresPort = 5432,
    redisPort = 6379,
  } = options;

  const header = `# ============================================
# ${projectName} - Environment Variables
# ============================================
# Generated by @flusk/cli
#
# IMPORTANT: Copy this file to .env and update with your values
#   cp .env.example .env
#
# NEVER commit .env to version control!
# ============================================`;

  return [
    header,
    envAppSection(),
    envDatabaseSection(projectName, postgresPort),
    envCacheSection(projectName, redisPort),
    envLlmSection(),
    envFluskSection(),
    envSecuritySection(),
    envLoggingSection(),
    envDockerSection(projectName),
    envDevToolsSection(),
    envTestingSection(projectName, postgresPort, redisPort),
    '',
  ].join('\n\n');
}
