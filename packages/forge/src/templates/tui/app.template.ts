/**
 * Template for the root TUI app with navigation
 */

import { toPascalCase } from '../../generators/utils.js';

export interface TuiAppScreen {
  name: string;
  key: string;
}

const DEFAULT_SCREENS: TuiAppScreen[] = [
  { name: 'overview', key: '1' },
  { name: 'patterns', key: '2' },
  { name: 'suggestions', key: '3' },
  { name: 'profile', key: '4' },
  { name: 'budget', key: '5' },
  { name: 'models', key: '6' },
  { name: 'history', key: '7' },
];

export function generateTuiAppTemplate(
  screens: TuiAppScreen[] = DEFAULT_SCREENS,
): string {
  const screenImports = screens
    .map((s) => {
      const pascal = toPascalCase(s.name);
      return `import { ${pascal}Screen } from './screens/${s.name}.js';`;
    })
    .join('\n');

  const screenType = screens.map((s) => `'${s.name}'`).join(' | ');

  const inputHandlers = screens
    .map((s) => `    if (input === '${s.key}') setScreen('${s.name}');`)
    .join('\n');

  const navLabel = screens
    .map((s) => `[${s.key}] ${toPascalCase(s.name)}`)
    .join('  ');

  const screenRenderers = screens
    .map((s) => {
      const pascal = toPascalCase(s.name);
      return `        {screen === '${s.name}' && (\n          <${pascal}Screen endpoint={endpoint} apiKey={apiKey} />\n        )}`;
    })
    .join('\n');

  return `/**
 * @generated by @flusk/cli — customize after generation
 * Root TUI App with keyboard navigation
 */

import React, { useState } from 'react';
// @ts-ignore ink types use exports field
import { render, Box, Text, useInput } from 'ink';
import { Header } from './components/header.js';
${screenImports}

type Screen = ${screenType};

interface AppProps {
  endpoint: string;
  apiKey: string;
}

function App({ endpoint, apiKey }: AppProps) {
  const [screen, setScreen] = useState<Screen>('${screens[0]?.name ?? 'overview'}');

  useInput((input: string) => {
${inputHandlers}
    if (input === 'q') process.exit(0);
  });

  return (
    <Box flexDirection="column">
      <Header endpoint={endpoint} />
      <Text dimColor>
        ${navLabel}  [q] Quit
      </Text>
      <Box marginTop={1}>
${screenRenderers}
      </Box>
    </Box>
  );
}

export async function renderDashboard(
  opts: { endpoint: string; apiKey?: string },
) {
  render(<App endpoint={opts.endpoint} apiKey={opts.apiKey || ''} />);
}
`;
}
