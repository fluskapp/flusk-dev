/**
 * Template for generating HTTP client files with SSRF protection,
 * retries, and standard CRUD methods.
 *
 * WHY: All HTTP clients follow the same pattern: URL validation,
 * fetch with error handling, retry logic. This template captures
 * that pattern so clients are consistent and secure by default.
 */

import { toPascalCase } from '../generators/utils.js';

export interface HttpClientTemplateOptions {
  clientName: string;
  baseUrlConfigKey: string;
}

export function generateHttpClientTemplate(
  opts: HttpClientTemplateOptions,
): string {
  const { clientName, baseUrlConfigKey } = opts;
  const pascal = toPascalCase(clientName);
  const envKey = baseUrlConfigKey.toUpperCase().replace(/-/g, '_');

  return `/**
 * @generated by @flusk/cli — http-client generator
 * ${pascal} HTTP Client
 */

// --- BEGIN GENERATED ---
import { request } from 'undici';
import { createLogger } from '@flusk/logger';

const log = createLogger({ name: '${clientName}-client' });

const PRIVATE_IP_PATTERNS = [
  /^127\\./,
  /^10\\./,
  /^172\\.(1[6-9]|2\\d|3[01])\\./,
  /^192\\.168\\./,
  /^169\\.254\\./,
  /^0\\./,
];

function validateUrl(rawUrl: string): void {
  const parsed = new URL(rawUrl);
  const isDev = process.env['NODE_ENV'] === 'development'
    || process.env['NODE_ENV'] === 'test';
  if (isDev) return;

  if (parsed.protocol !== 'https:') {
    throw new Error(\`URL must use HTTPS (got \${parsed.protocol})\`);
  }
  const host = parsed.hostname;
  if (host === '::1' || host === 'localhost' || host === '127.0.0.1'
    || host.startsWith('fc') || host.startsWith('fd')) {
    throw new Error(\`URL blocked: private/reserved address (\${host})\`);
  }
  for (const pattern of PRIVATE_IP_PATTERNS) {
    if (pattern.test(host)) {
      throw new Error(\`URL blocked: private IP range (\${host})\`);
    }
  }
}

interface RetryOptions {
  maxRetries?: number;
  baseDelayMs?: number;
}

async function withRetry<T>(
  fn: () => Promise<T>,
  opts: RetryOptions = {},
): Promise<T> {
  const maxRetries = opts.maxRetries ?? 3;
  const baseDelay = opts.baseDelayMs ?? 500;
  let lastError: unknown;
  for (let attempt = 0; attempt <= maxRetries; attempt++) {
    try {
      return await fn();
    } catch (err) {
      lastError = err;
      if (attempt < maxRetries) {
        const delay = baseDelay * Math.pow(2, attempt);
        await new Promise((r) => setTimeout(r, delay));
        log.warn({ attempt, err }, '${clientName} request failed, retrying');
      }
    }
  }
  throw lastError;
}
// --- END GENERATED ---

// --- BEGIN CUSTOM ---
export class ${pascal}Client {
  private readonly baseUrl: string;
  private readonly retryOpts: RetryOptions;

  constructor(baseUrl?: string, retryOpts?: RetryOptions) {
    this.baseUrl = baseUrl
      ?? process.env['${envKey}_URL']
      ?? 'http://localhost:3000';
    this.retryOpts = retryOpts ?? {};
    validateUrl(this.baseUrl);
  }

  async get<T = unknown>(path: string): Promise<T> {
    const url = \`\${this.baseUrl}\${path}\`;
    validateUrl(url);
    return withRetry(async () => {
      const res = await request(url, { method: 'GET' });
      const body = await res.body.json() as T;
      log.info({ url, status: res.statusCode }, '${clientName} GET');
      return body;
    }, this.retryOpts);
  }

  async post<T = unknown>(path: string, data: unknown): Promise<T> {
    const url = \`\${this.baseUrl}\${path}\`;
    validateUrl(url);
    return withRetry(async () => {
      const res = await request(url, {
        method: 'POST',
        headers: { 'content-type': 'application/json' },
        body: JSON.stringify(data),
      });
      const body = await res.body.json() as T;
      log.info({ url, status: res.statusCode }, '${clientName} POST');
      return body;
    }, this.retryOpts);
  }

  async put<T = unknown>(path: string, data: unknown): Promise<T> {
    const url = \`\${this.baseUrl}\${path}\`;
    validateUrl(url);
    return withRetry(async () => {
      const res = await request(url, {
        method: 'PUT',
        headers: { 'content-type': 'application/json' },
        body: JSON.stringify(data),
      });
      const body = await res.body.json() as T;
      log.info({ url, status: res.statusCode }, '${clientName} PUT');
      return body;
    }, this.retryOpts);
  }

  async delete(path: string): Promise<void> {
    const url = \`\${this.baseUrl}\${path}\`;
    validateUrl(url);
    return withRetry(async () => {
      const res = await request(url, { method: 'DELETE' });
      log.info({ url, status: res.statusCode }, '${clientName} DELETE');
    }, this.retryOpts);
  }
}
// --- END CUSTOM ---
`;
}
