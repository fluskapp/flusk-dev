/**
 * Template for generating full CRUD route files from entity YAML.
 *
 * WHY: 8+ route files in execution/routes are 90% identical CRUD
 * boilerplate. This template captures the pattern so generators
 * produce consistent, complete route files.
 */

import { toPascalCase, toCamelCase } from '../generators/utils.js';
import { buildHandlersSuffix } from './crud-routes-handlers.template.js';

export interface CrudRoutesTemplateOptions {
  entityName: string;
}

export function generateCrudRoutesTemplate(
  opts: CrudRoutesTemplateOptions,
): string {
  const { entityName } = opts;
  const pascal = toPascalCase(entityName);
  const camel = toCamelCase(entityName);

  return `/**
 * @generated by @flusk/cli — crud-routes recipe
 */

// --- BEGIN GENERATED ---
import type { FastifyInstance } from 'fastify';
import { Type, type TSchema } from '@sinclair/typebox';
import { ${pascal}EntitySchema } from '@flusk/entities';
import { ${pascal}Repository } from '@flusk/resources';
// --- END GENERATED ---

// --- BEGIN CUSTOM ---
// eslint-disable-next-line @typescript-eslint/no-explicit-any -- TypeBox Type.Omit requires TObject cast
const Create${pascal}Schema = Type.Omit(${pascal}EntitySchema as any, ['id', 'createdAt', 'updatedAt']);
const ${pascal}ResponseSchema = ${pascal}EntitySchema;
const IdParamsSchema = Type.Object({ id: Type.String({ format: 'uuid' }) });
const NotFoundSchema = Type.Object({ error: Type.String() });
const ListQuerySchema = Type.Object({
  limit: Type.Optional(Type.Integer({ minimum: 1 })),
  offset: Type.Optional(Type.Integer({ minimum: 0 })),
});

export async function ${camel}Routes(
  fastify: FastifyInstance,
): Promise<void> {
  fastify.post('/', {
    schema: {
      body: Create${pascal}Schema,
      response: { 201: ${pascal}ResponseSchema as unknown as TSchema },
      tags: ['${pascal}'],
      description: 'Create a new ${pascal} record',
    },
  }, async (request, reply) => {
    // eslint-disable-next-line @typescript-eslint/no-explicit-any -- request body validated by schema
    const created = ${pascal}Repository.create${pascal}(fastify.db, request.body as any);
    return reply.code(201).send(created);
  });

  fastify.get('/:id', {
    schema: {
      params: IdParamsSchema,
      response: { 200: ${pascal}ResponseSchema as unknown as TSchema, 404: NotFoundSchema },
      tags: ['${pascal}'],
      description: 'Get ${pascal} by ID',
    },
  }, async (request, reply) => {
    const { id } = request.params as { id: string };
    const entity = ${pascal}Repository.find${pascal}ById(fastify.db, id);
    if (!entity) return reply.code(404).send({ error: 'Not found' });
    return reply.code(200).send(entity);
  });

  fastify.get('/', {
    schema: {
      querystring: ListQuerySchema,
      response: { 200: Type.Array(${pascal}ResponseSchema as unknown as TSchema) },
      tags: ['${pascal}'],
      description: 'List ${pascal} records',
    },
  }, async (request, reply) => {
    const { limit, offset } = request.query as { limit?: number; offset?: number };
    const items = ${pascal}Repository.list${pascal}s(fastify.db, limit, offset);
    return reply.code(200).send(items);
  });

` + buildHandlersSuffix(entityName);
}
