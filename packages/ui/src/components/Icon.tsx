export const Icon: React.FC<IconProps> = ({
  name,
  size = 'md',
  color = 'current',
  className,
  'aria-hidden': ariaHidden = true,
  ...props
}) => {
  const iconPath = icons[name];
  
  if (!iconPath) {
    console.warn(`Icon "${name}" not found in icon library`);
    return null;
  }

  const isFilled = name === 'github' || name === 'twitter' || name === 'linkedin';

  return (
    <svg
      className={cn(
        iconSizes[size],
        iconColors[color],
        'flex-shrink-0',
        className
      )}
      fill={isFilled ? 'currentColor' : 'none'}
      stroke={isFilled ? 'none' : 'currentColor'}
      viewBox="0 0 24 24"
      aria-hidden={ariaHidden}
      {...props}
    >
      {iconPath}
    </svg>
  );
};

// 🎯 Icon Button - Icon with button functionality
export interface IconButtonProps extends React.ButtonHTMLAttributes<HTMLButtonElement> {
  icon: IconProps['name'];
  iconSize?: IconProps['size'];
  variant?: 'default' | 'primary' | 'secondary' | 'ghost' | 'danger';
  size?: 'sm' | 'md' | 'lg';
  isLoading?: boolean;
  className?: string;
  'aria-label': string; // Required for accessibility
}

const iconButtonVariants = {
  default: 'bg-gray-100 hover:bg-gray-200 text-gray-600 hover:text-gray-700',
  primary: 'bg-primary-100 hover:bg-primary-200 text-primary-600 hover:text-primary-700',
  secondary: 'bg-secondary-100 hover:bg-secondary-200 text-secondary-600 hover:text-secondary-700',
  ghost: 'bg-transparent hover:bg-gray-100 text-gray-500 hover:text-gray-700',
  danger: 'bg-error-100 hover:bg-error-200 text-error-600 hover:text-error-700',
};

const iconButtonSizes = {
  sm: 'p-1.5',
  md: 'p-2',
  lg: 'p-3',
};

export const IconButton: React.FC<IconButtonProps> = ({
  icon,
  iconSize = 'md',
  variant = 'default',
  size = 'md',
  isLoading = false,
  className,
  disabled,
  'aria-label': ariaLabel,
  ...props
}) => {
  return (
    <button
      className={cn(
        'inline-flex items-center justify-center rounded-lg border-0',
        'transition-colors duration-200 ease-in-out',
        'focus:outline-none focus:ring-2 focus:ring-primary-500 focus:ring-offset-2',
        'disabled:opacity-50 disabled:cursor-not-allowed',
        iconButtonVariants[variant],
        iconButtonSizes[size],
        className
      )}
      disabled={disabled || isLoading}
      aria-label={ariaLabel}
      {...props}
    >
      {isLoading ? (
        <Icon name="spinner" size={iconSize} className="animate-spin" />
      ) : (
        <Icon name={icon} size={iconSize} />
      )}
    </button>
  );
};