/**
 * Unit tests for detector generator
 */

import { test, describe } from 'node:test';
import assert from 'node:assert';
import { generateDetectorContent, generateDetectorTestContent } from './detector.generator.js';

describe('Detector Generator', () => {
  test('generates detector function', () => {
    const content = generateDetectorContent('hot-path-calls');
    assert.ok(content.includes('detectHotPathCalls'));
    assert.ok(content.includes('DetectedPattern[]'));
    assert.ok(content.includes('CorrelationResult'));
  });

  test('includes @generated header', () => {
    const content = generateDetectorContent('test');
    assert.ok(content.includes('@generated by @flusk/cli'));
  });

  test('uses pattern name in output', () => {
    const content = generateDetectorContent('memory-churn');
    assert.ok(content.includes("pattern: 'memory-churn'"));
  });

  test('generates test file', () => {
    const content = generateDetectorTestContent('hot-path-calls');
    assert.ok(content.includes('detectHotPathCalls'));
    assert.ok(content.includes('toEqual([])'));
  });
});
