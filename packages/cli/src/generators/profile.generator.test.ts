/**
 * Unit tests for profile generator
 */

import { test, describe } from 'node:test';
import assert from 'node:assert';
import {
  generateProfileConfigContent,
  generateProfileScriptContent,
} from './profile.generator.js';

describe('Profile Generator', () => {
  test('generates profile config with required fields', () => {
    const content = generateProfileConfigContent();
    assert.ok(content.includes('sourcemapDirs'));
    assert.ok(content.includes('outputDir'));
    assert.ok(content.includes('./profiles'));
    assert.ok(content.includes("format: 'md'"));
  });

  test('generates profile script with bash shebang', () => {
    const content = generateProfileScriptContent();
    assert.ok(content.startsWith('#!/usr/bin/env bash'));
    assert.ok(content.includes('set -euo pipefail'));
    assert.ok(content.includes('DURATION='));
  });

  test('config includes generated header comment', () => {
    const content = generateProfileConfigContent();
    assert.ok(content.includes('@generated by @flusk/cli'));
  });

  test('script includes generated header comment', () => {
    const content = generateProfileScriptContent();
    assert.ok(content.includes('@generated by @flusk/cli'));
  });
});
