/**
 * Types generator - creates TypeScript types and JSON schemas from entity definitions
 */

import { resolve } from 'node:path';
import { writeFile, mkdir } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import { toPascalCase } from './utils.js';

export interface GeneratorResult {
  path: string;
  content: string;
}

export async function generateTypes(
  entityPath: string,
  entityName: string
): Promise<GeneratorResult> {
  const pascalName = toPascalCase(entityName);
  const outputDir = resolve(process.cwd(), 'packages/types/src');
  const outputPath = resolve(outputDir, `${entityName}.types.ts`);

  if (!existsSync(outputDir)) {
    await mkdir(outputDir, { recursive: true });
  }

  const content = `/**
 * @generated by @flusk/cli from ${entityName}.entity.ts
 * DO NOT EDIT - regenerate using: flusk g ${entityName}.entity.ts
 */

import { Type, Static } from '@sinclair/typebox';
import { ${pascalName}EntitySchema, BaseEntitySchema } from '@flusk/entities';

/**
 * TypeScript type for ${pascalName}Entity
 * Generated from ${pascalName}EntitySchema in entities package
 */
export type ${pascalName}Entity = Static<typeof ${pascalName}EntitySchema>;

/**
 * TypeScript type for BaseEntity
 */
export type BaseEntity = Static<typeof BaseEntitySchema>;

/**
 * JSON Schema for ${pascalName}Entity
 * Use for validation and API documentation
 */
export const ${pascalName}EntityJSONSchema = ${pascalName}EntitySchema;

/**
 * Insert variant - excludes auto-generated fields (id, createdAt, updatedAt)
 */
export const ${pascalName}InsertSchema = Type.Omit(${pascalName}EntitySchema, [
  'id',
  'createdAt',
  'updatedAt'
]);

export type ${pascalName}Insert = Static<typeof ${pascalName}InsertSchema>;

/**
 * Update variant - all fields optional except id
 */
export const ${pascalName}UpdateSchema = Type.Composite([
  Type.Object({ id: Type.String({ format: 'uuid' }) }),
  Type.Partial(Type.Omit(${pascalName}EntitySchema, ['id', 'createdAt', 'updatedAt']))
]);

export type ${pascalName}Update = Static<typeof ${pascalName}UpdateSchema>;

/**
 * Query variant - all fields optional for flexible filtering
 */
export const ${pascalName}QuerySchema = Type.Partial(${pascalName}EntitySchema);

export type ${pascalName}Query = Static<typeof ${pascalName}QuerySchema>;
`;

  await writeFile(outputPath, content, 'utf-8');

  return {
    path: `types/${entityName}.types.ts`,
    content
  };
}
