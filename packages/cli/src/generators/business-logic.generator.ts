/**
 * Business logic generator - creates pure function stubs
 */

import { resolve } from 'node:path';
import { writeFile, mkdir } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import { toPascalCase } from './utils.js';

export interface GeneratorResult {
  path: string;
  content: string;
}

export async function generateBusinessLogic(
  entityPath: string,
  entityName: string
): Promise<GeneratorResult[]> {
  const results: GeneratorResult[] = [];
  const pascalName = toPascalCase(entityName);
  const outputDir = resolve(process.cwd(), `packages/business-logic/src/${entityName}`);

  if (!existsSync(outputDir)) {
    await mkdir(outputDir, { recursive: true });
  }

  // Generate example function stub
  const exampleFunc = await generateExampleFunction(entityName, pascalName, outputDir);
  results.push(exampleFunc);

  // Generate index file
  const indexFile = await generateIndex(entityName, outputDir);
  results.push(indexFile);

  return results;
}

async function generateExampleFunction(
  entityName: string,
  pascalName: string,
  outputDir: string
): Promise<GeneratorResult> {
  const outputPath = resolve(outputDir, `validate-${entityName}.function.ts`);

  const content = `/**
 * @generated by @flusk/cli from ${entityName}.entity.ts
 * Business logic stub - implement your domain logic here
 */

import { ${pascalName}Entity } from '@flusk/types';

/**
 * Example validation function for ${pascalName}
 * Pure function - no side effects, no I/O
 */
export function validate${pascalName}(
  entity: Partial<${pascalName}Entity>
): { valid: boolean; errors: string[] } {
  const errors: string[] = [];

  // TODO: Add validation logic
  // Example:
  // if (!entity.someField) {
  //   errors.push('someField is required');
  // }

  return {
    valid: errors.length === 0,
    errors
  };
}
`;

  await writeFile(outputPath, content, 'utf-8');

  return {
    path: `business-logic/${entityName}/validate-${entityName}.function.ts`,
    content
  };
}

async function generateIndex(
  entityName: string,
  outputDir: string
): Promise<GeneratorResult> {
  const outputPath = resolve(outputDir, 'index.ts');
  const pascalName = toPascalCase(entityName);

  const content = `/**
 * @generated by @flusk/cli from ${entityName}.entity.ts
 */

export { validate${pascalName} } from './validate-${entityName}.function.js';
`;

  await writeFile(outputPath, content, 'utf-8');

  return {
    path: `business-logic/${entityName}/index.ts`,
    content
  };
}
